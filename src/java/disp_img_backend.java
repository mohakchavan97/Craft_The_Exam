            out.println("<body>");
//            out.println("<h1>Servlet disp_img_backend at " + request.getContextPath() + "</h1>");

            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/craft_the_exam", "root", "");
            Statement st = (Statement) con.createStatement();
            ResultSet rs = st.executeQuery("SELECT `image` FROM `questions` WHERE `Que_ID`=9");
            Blob img=rs.getBlob(1);
            byte[] imgData = img.getBytes(1, (int)img.length());
            response.setContentType("image/jpg");
            OutputStream o=response.getOutputStream();
            o.write(imgData);
            o.flush();
            o.close();
            
            out.println("</body>");
            out.println("</html>");
